// Increment(++) operator ->

let x=4
let y=x++
console.log(`x:${x}, y:${y}`); // x:5, y:4

let a=7
let b=++a
console.log(`a is ${a} and b is ${b}`); // a is 8 and b is 8



// decrement (--) operator ->

let x1 = 5n; 
let y1 = x1--;
console.log(`x:${x1}, y:${y1}`); // x:4, y:5

let x2 = 5n
let y2 = --x2
console.log(`x:${x2}, y:${y2}`); // x:4, y:4
